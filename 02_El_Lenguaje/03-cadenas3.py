#metodo format
cadena = "Hola, me llamo {} y tengo {} años"
print(cadena.format("Ivan", 25))
cadena = "Hola, me llamo {} y tengo {} años"
resultado = cadena.format("Ivan", 25)
print(resultado)
#el beneficio de guardar el resultado de la operacion en una variable es que podemos usarlo en varias ocasiones sin tener que repetir la operacion
