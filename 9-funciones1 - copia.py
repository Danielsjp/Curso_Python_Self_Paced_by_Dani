#print es una funcion porque tiene un nombre y un conjunto de instrucciones que se ejecutan cuando se llama a la funcion.
print("hola") 
#se pueden pasar varios argumentos a la funcion print, separados por comas.
print("Hola", ", buen dia", "como estas?") 
#se pueden pasar varios argumentos a la funcion print, separados por comas y en varias lineas.
#esto es util cuando se quiere imprimir un mensaje largo y se quiere mantener el codigo legible.
print("Me gusta", 
"tomar cafe", 
"por la mañana") 
#se pueden pasar varios argumentos a la funcion print, separados por comas y en varias lineas.
#estos datos pueden ser de diferentes tipos, como enteros, cadenas de texto, etc. La funcion print los convierte a cadena de texto y los imprime en la consola.
print (1, "hola", 3+4, "adios") 
#se puede cambiar el separador entre los argumentos de la funcion print, usando el parametro sep. Por defecto, el separador es un espacio en blanco, pero se puede cambiar a cualquier otro caracter o cadena de texto.
#end hace que al final de la impresion se agregue un caracter o cadena de texto, en lugar de un salto de linea. Por defecto, end es un salto de linea, pero se puede cambiar a cualquier otro caracter o cadena de texto.
print("hola", "adios", "buenas tardes", sep="---", end=";")

